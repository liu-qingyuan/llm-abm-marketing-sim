REQUIRED_I18N_KEYS = frozenset(REPORT_I18N["en-US"].keys())


def validate_i18n_key_parity() -> None:
    expected = REQUIRED_I18N_KEYS
    for language in SUPPORTED_LANGUAGES:
        actual = set(REPORT_I18N[language])
        missing = expected - actual
        extra = actual - expected
        if missing or extra:
            raise ValueError(f"i18n key mismatch for {language}: missing={sorted(missing)} extra={sorted(extra)}")
